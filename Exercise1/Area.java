package Exercise1;

public class Area {

	void squareArea(int length, int width) {
		float area;
		area= length*width;
		System.out.println("Area of Square="+ area);
	}
	void circleArea(int radius) {
		double area;
		area=3.14*radius*radius;
		System.out.println("Area of circle="+ area);
	}
	
	
}
